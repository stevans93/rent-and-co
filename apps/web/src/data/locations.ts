// Get all cities for a country
export function getCitiesForCountry(countryName: string): City[] {
  const country = countries.find(c => c.name === countryName);
  return country?.cities || [];
}

// Get municipalities for a city
export function getMunicipalitiesForCity(countryName: string, cityName: string): Municipality[] {
  const country = countries.find(c => c.name === countryName);
  const city = country?.cities.find(c => c.name === cityName);
  return city?.municipalities || [];
}

// Get all country names
export function getCountryNames(): string[] {
  return countries.map(c => c.name);
}

// Search cities across all countries
export function searchCities(query: string): { country: string; city: City }[] {
  const results: { country: string; city: City }[] = [];
  const lowerQuery = query.toLowerCase();
  
  countries.forEach(country => {
    country.cities.forEach(city => {
      if (city.name.toLowerCase().includes(lowerQuery)) {
        results.push({ country: country.name, city });
      }
    });
  });
  
  return results.slice(0, 20); // Limit results
}
